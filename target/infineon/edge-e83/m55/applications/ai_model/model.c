/*
*  @param data_in args_0. Input float[292].
*  @param data_out output_0. Output float[5].
*  @return IPWIN_RET_SUCCESS (0) or IPWIN_RET_NODATA (-1), IPWIN_RET_ERROR (-2), IPWIN_RET_STREAMEND (-3)
*/
int IMAI_compute(const float *restrict data_in, float *restrict data_out) {    
    __HOOK_REGION(true, 0);
    __RETURN_ERROR(mtb_model_set_input_float32(_K3, 0, data_in));
    __HOOK_REGION(false, 0);
    __RETURN_ERROR(mtb_model_invoke(_K3));
    __HOOK_REGION(true, 2);
    __RETURN_ERROR(mtb_model_get_output_float32(_K3, 0, data_out));
    __HOOK_REGION(false, 2);
    return 0;
}

