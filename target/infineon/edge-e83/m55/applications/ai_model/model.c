static int mtb_quantize_to_int8(const float* in, int8_t* out, int32_t size, float scale, int32_t zero_point)
{
	int loop_count;
	float val;

	/* Sanity check of input parameters */
	if (in == NULL || out == NULL || size <= 0)
	{
		return IMAI_RET_ERROR;
	}

#if defined(COMPONENT_CMSIS_DSP)
	/* Process 4 output at one time */
	loop_count = size;
	while (loop_count > 0)
	{
		val = *in++;
		val = (val / scale) + zero_point;
		val += val > 0.0f ? 0.5f : -0.5f;
		*out++ = (int8_t) (__SSAT((int32_t) (val), 8));
		loop_count--;
	}
#else
	loop_count = size;
	while (loop_count > 0)
	{
		val = (*in++ / scale) + zero_point;
		val += val > 0.0f ? 0.5f : -0.5f;
		if ((int32_t) val > SCHAR_MAX)
			*out++ = SCHAR_MAX;
		else if ((int32_t) val < SCHAR_MIN)
			*out++ = SCHAR_MIN;
		else
			*out++ = (int8_t) (val);

		loop_count--;
	}
#endif /* COMPONENT_CMSIS_DSP */

	return IMAI_RET_SUCCESS;
}

