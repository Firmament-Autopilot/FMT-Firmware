static inline int mtb_model_set_input_float32(const void* handle, int input_index, const float* restrict data)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;

	if (input_index >= model->input_count || model->inputs == NULL)
		return IPWIN_RET_ERROR;

	MTB_ML_DATA_T* input_ptr = model->inputs[input_index].ptr;
	size_t num_elements = model->inputs[input_index].elements;
	uint8_t type_size = model->inputs[input_index].type_size;
	float scale = model->inputs[input_index].scale;
	int zero_point = model->inputs[input_index].zero_point;
	
	// Quantize float to model's native type
	if (type_size == 1) {
		// Float32 → Int8
		int8_t* quantized = (int8_t*)input_ptr;

		mtb_quantize_to_int8(data, quantized, num_elements, scale, zero_point);
	}
	else if (type_size == 2) {
		// Float32 → Int16
		int16_t* quantized = (int16_t*)input_ptr;
		
		mtb_quantize_to_int16(data, quantized, num_elements, scale, zero_point);
	}
	else {
		// Float32 → Float32 (direct copy)
		memcpy(input_ptr, data, num_elements * sizeof(float));
	}

	return IPWIN_RET_SUCCESS;
}

static inline int mtb_model_invoke(const void* handle)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;
	
	mtb_ml_model_invoke(model);
	
	int ret_status = model->lib_error;
	if (ret_status != 0) { printf("ERROR: TensorFlow Lite model inference failed with error code: %d.\r\n", ret_status); }
	CY_ASSERT(ret_status == 0);
	
	return IPWIN_RET_SUCCESS;
}

static inline int mtb_model_get_output_float32(const void* handle, int output_index, float* restrict data)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;

	if (output_index >= model->output_count || model->outputs == NULL)
		return IPWIN_RET_ERROR;

	MTB_ML_DATA_T* output_ptr = model->outputs[output_index].ptr;
	size_t num_elements = model->outputs[output_index].elements;
	uint8_t type_size = model->outputs[output_index].type_size;
	float scale = model->outputs[output_index].scale;
	int zero_point = model->outputs[output_index].zero_point;
	
	// Dequantize model's native type to float
	if (type_size == 1) {
		// Int8 → Float32
		const int8_t* quantized = (const int8_t*)output_ptr;

		for (size_t i = 0; i < num_elements; i++)
			data[i] = ((float)quantized[i] - zero_point) * scale;
	}
	else if (type_size == 2) {
		// Int16 → Float32
		const int16_t* quantized = (const int16_t*)output_ptr;
		
		for (size_t i = 0; i < num_elements; i++)
			data[i] = ((float)quantized[i] - zero_point) * scale;
	}
	else {
		// Float32 → Float32 (direct copy)
		memcpy(data, output_ptr, num_elements * sizeof(float));
	}

	return IPWIN_RET_SUCCESS;
}

static inline void mtb_model_free(const void* handle)
{
	mtb_ml_model_t* model = *(mtb_ml_model_t**)handle;
	mtb_ml_model_deinit(model);

#ifndef IMAI_NO_NPU
	mtb_ml_deinit();
#endif

	if (IMAI_mtb_models_count > 0)
		IMAI_mtb_models_count--;
}

/// @brief Quantize a floating-point value to a quantized integer
/// @param in The source pointer to the floating-point value
/// @param out The destination pointer to the quantized integer
/// @param size The number of elements in the quantized integer
/// @param scale The scale of the quantized integer
/// @param zero_point The offset of the quantized integer
/// @return IMAI_RET_SUCCESS on success, IMAI_RET_ERROR on error
int IMAI_quantize(const float* in, int8_t *out, int32_t size, float scale, int32_t zero_point)
{
	return mtb_quantize_to_int8(in, out, size, scale, zero_point);
}

/// @brief Dequantize a quantized integer to a floating-point value
/// @param src The source pointer to the quantized integer
/// @param dst The destination pointer to the floating-point value
/// @param count The number of elements in the quantized integer
/// @param scale The scale of the quantized integer
/// @param offset The offset of the quantized integer
/// @return IMAI_RET_SUCCESS on success, IMAI_RET_ERROR on error
int IMAI_dequantize(const int8_t *restrict in, float *restrict out, int32_t count, float scale, int32_t zero_point)
{
	for (int i = 0; i < count; i++)
		out[i] = ((float)in[i] - (float)zero_point) * scale;
	return IMAI_RET_SUCCESS;
}

