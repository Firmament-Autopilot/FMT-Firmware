static void mf_align_count_features(float* features)
{
    unsigned count = sizeof(mf_count_feature_index) / sizeof(mf_count_feature_index[0]);

    for (unsigned i = 0U; i < count; i++) {
        unsigned index = mf_count_feature_index[i];
        features[index] = -mf_feature_bias[index] / mf_feature_scale[index];
    }
}

void motor_fault_preprocess_features(float* features, unsigned feature_count)
{
    if (features == 0 || feature_count != MOTOR_FAULT_FEATURE_COUNT) {
        return;
    }

    mf_align_count_features(features);

    for (unsigned i = 0U; i < MOTOR_FAULT_FEATURE_COUNT; i++) {
        features[i] = features[i] * mf_feature_scale[i] + mf_feature_bias[i];
    }
}
